#!/usr/local/bin/python3

#Declare a list
List = [2, 3, "Alex", 6]
#Let's first print the list in the order it appears in the array
print(List[:])
#Now, we want to output the  reversed list
print(List[::-1])
"""Now, I want to access a value in index 2, using both the normal order
and reversed sequence order"""
#Normal order
print(List[2])
#reversed index order
print(List[-2])

